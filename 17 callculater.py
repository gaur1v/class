a=int(input("Enter the value 1:"))
b=int(input("Enter the value 2:"))
opr=input("Enter the opr:")
if opr=="-":
    print(a-b)
elif opr=="+":
    print(a+b)
elif opr=="*":
    print(a*b)
elif opr=="/":
    print(a/b)
elif opr=="//":
    print(a//b)
else:
    print("invalid value")







